    private void btnBuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuardarActionPerformed
        Precio precio = new Precio();
        precio.setNombre(txtNombre.getText());
        precio.setAccion(cboAccion.getSelectedItem().toString());
        precio.setMonto(Double.parseDouble(txtMonto.getText()));
        GestionarCartaPrecioServicio gestionarCartaPrecioServicio = new GestionarCartaPrecioServicio();

        try {
            int registros_afectados = gestionarCartaPrecioServicio.crearPrecio(precio);
            if (registros_afectados == 1) {
                Mensaje.mostrarAfirmacionDeCreacion(this);
                this.dispose();
            } else {
                Mensaje.mostrarAdvertenciaDeCreacion(this);
            }
        } catch (Exception e) {
            Mensaje.mostrarAdvertencia(this, e.getMessage());
        }
    }//GEN-LAST:event_btnBuardarActionPerformed

